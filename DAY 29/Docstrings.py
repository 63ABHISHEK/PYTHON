# What is Docstrings?
# Docstrings are the string literals that appear right after the definition of a function, method, class, or module.
# Docstrings are used to document our code.
# Docstrings are not ignored like comments.
# Docstrings are not displayed when the help command is used.
# Docstrings are not displayed when the __doc__ attribute is accessed.

# What is the difference between comment and docstring?

def sqaure(n):
  '''Takes in a number n, returns the sqaure of n'''
  print(n**2)
sqaure(5)
print(sqaure.__doc__)

