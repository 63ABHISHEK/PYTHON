print("Hey I am a good boy.\nand the viewer is also a good boy/girl")
# Here is \n is a escape sequence character.
# It is used to print the text in the next line.

# \t is used to give a tab space.
print("Hey I am a good boy.\tand the viewer is also a good boy/girl")

# \b is used to give a backspace.
print("Hey I am a good boy.\band the viewer is also a good boy/girl")

# \r is used to give a carriage return.
print("Hey I am a good boy.\rand the viewer is also a good boy/girl")

# \f is used to give a form feed.
print("Hey I am a good boy.\fand the viewer is also a good boy/girl")

# \v is used to give a vertical tab.
print("Hey I am a good boy.\vand the viewer is also a good boy/girl")

# \a is used to give a alert.
print("Hey I am a good boy.\aand the viewer is also a good boy/girl")

# \ooo is used to give a octal value.
print("Hey I am a good boy.\123and the viewer is also a good boy/girl")

# \xhh is used to give a hexadecimal value.
print("Hey I am a good boy.\x23and the viewer is also a good boy/girl")

# \\ is used to give a backslash.
print("Hey I am a good boy.\\and the viewer is also a good boy/girl")

# \' is used to give a single quote.
print("Hey I am a good boy.\'and the viewer is also a good boy/girl")

# \" is used to give a double quote.
print("Hey I am a good boy.\"and the viewer is also a good boy/girl")

# \ is used to give a backslash.
